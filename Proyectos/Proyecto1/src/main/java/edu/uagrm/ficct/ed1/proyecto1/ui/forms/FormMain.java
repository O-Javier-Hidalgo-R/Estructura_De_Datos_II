    /**
     * Procedimiento que selecciona el modo de visualizacion en funcion del
     * estado de la bandera "modeDark".
     *
     * @param evt
     */
    private void IUModeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IUModeButtonActionPerformed
        // TODO add your handling code here:
        if (FormMain.modeDark) {
            FlatArcDarkOrangeIJTheme.setup();
            java.awt.EventQueue.invokeLater(() -> {
                FormMain.modeDark = false;
                new FormMain().setVisible(true);
                this.dispose();
            });
        } else {
            FlatArcOrangeIJTheme.setup();
            java.awt.EventQueue.invokeLater(() -> {
                FormMain.modeDark = true;
                new FormMain().setVisible(true);
                this.dispose();
            });
        }
    }//GEN-LAST:event_IUModeButtonActionPerformed

